package MRD.Constructors;

// Creating constructors
/*
* for constructors to be there, attributes should be there*/

public class Main {
    // Attributes
    int phoneNumber, address;
    String firstName, lastName, email;

    // MRD.Constructors
    public Main(int phoneNumber, int address, String firstName, String lastName, String email) {
        this.phoneNumber = phoneNumber;
        this.address = address;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }
}
